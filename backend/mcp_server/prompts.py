"""
MCP Prompts 实现

实现所有 MCP Prompt 模板，提供预定义的提示词供用户使用
"""

from datetime import datetime


class FundValuationPrompts:
    """基金估值系统 MCP Prompts"""

    def analyze_fund(self, fund_code: str) -> str:
        """
        分析单只基金的投资价值提示词

        Args:
            fund_code: 基金代码
        """
        return f"""请分析基金 {fund_code} 的投资价值。

请按照以下结构进行分析：

## 1. 基金基本信息
- 基金名称和代码
- 基金类型（股票型/混合型/债券型/QDII 等）
- 成立日期和规模
- 费率结构

## 2. 估值分析
使用 `get_valuation` 工具获取该基金的实时估值，分析：
- 当前估算净值和涨跌幅
- 估值类型和置信度
- 与前一交易日净值的对比

## 3. 持仓分析
- 前十大重仓股/资产
- 行业分布
- 区域分布（如为 QDII）

## 4. 风险收益特征
- 历史业绩表现
- 波动率和回撤
- 夏普比率等风险调整后收益

## 5. 投资建议
- 适合的投资者类型
- 配置建议
- 风险提示

请先调用 `get_fund_info` 和 `get_valuation` 工具获取该基金的详细信息。"""

    def portfolio_summary(self) -> str:
        """生成持仓组合总结报告提示词"""
        return f"""请生成我的基金持仓组合总结报告（日期：{datetime.now().strftime('%Y-%m-%d')}）。

请按照以下结构生成报告：

## 1. 组合概览
- 持仓基金数量
- 总资产价值
- 当日盈亏估算

## 2. 各基金表现
对每只持仓基金，使用 `get_valuation` 工具获取实时估值，列出：
- 基金名称和代码
- 持有份额和当前价值
- 当日估算涨跌幅和盈亏
- 估值类型和置信度

## 3. 资产配置分析
- 各基金类型占比（股票型/混合型/债券型/QDII 等）
- 行业分布（基于底层资产）
- 区域分布（A 股/港股/美股等）

## 4. 风险提示
- 集中度风险
- 行业/区域风险
- 汇率风险（如持有 QDII）

## 5. 调整建议
- 再平衡建议
- 优化方向

请先调用 `get_fund_list` 获取所有持仓基金，然后调用 `get_batch_valuation` 获取实时估值。"""

    def market_daily(self) -> str:
        """生成市场日报提示词"""
        return f"""请生成今日市场日报（{datetime.now().strftime('%Y-%m-%d %H:%M')}）。

请按照以下结构生成日报：

## 1. 国内市场
获取以下指数的实时数据（使用 `get_index_price` 工具）：
- 沪深 300 (000300)
- 中证 500 (000905)
- 创业板指 (399006)
- 科创 50 (000688)
- 上证 50 (000016)

## 2. 海外市场
获取以下指数的实时数据（使用 `get_global_index_price` 工具）：
- 纳斯达克 (nasdaq)
- 标普 500 (sp500)
- 道琼斯 (dji)
- 恒生指数 (hsi)
- 日经 225 (n225)

## 3. ETF 市场
获取主要 ETF 的实时数据（使用 `get_etf_price` 工具）：
- 沪深 300ETF (510300)
- 中证 500ETF (510500)
- 创业板 ETF (159915)
- 科创 50ETF (588000)

## 4. 市场点评
- 各市场涨跌情况总结
- 领涨/领跌板块
- 资金流向

## 5. 估值温度计
- 主要指数的估值水平
- 市场情绪判断

## 6. 操作建议
- 定投入场建议
- 再平衡提示

请依次调用上述工具获取市场数据，然后进行综合分析。"""
